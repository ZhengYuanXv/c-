#include <iostream>
using namespace std;
int main()
{
	//创建bool数据类型
	bool flag = true; //turn代表真
	cout << flag << endl;
	flag = false; //flase代表假
	cout << flag <<endl;
	
	//两个都占一个字节
	
	
	
	
	
	
	
	
	bool d = 0||0;//或 逻辑运算符
	d = 0&&22;//与 逻辑运算符
	d = !true;//非 逻辑运算符(取反值)
	cout<<d;
	
	
	
	
	
	
	
	return 0;
	
}
